# Integer
a = 4
b = 2


c = a+b
d = a/b
print('a = ', a, 'b = ', b, 'c = ', c, 'd = ', d)
print('type of a =', type(a), 'type of b = ', type(b), 'type of c = ', type(c), 'type of d = ', type(d))
a, b = b, a
print(a, b)

